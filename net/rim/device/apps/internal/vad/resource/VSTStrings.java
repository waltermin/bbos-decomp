   public static final String[] loadStrings(byte[] data, String encoding, boolean all) {
      try {
         int totalStrings = readInt(data, 28) - 1;
         int numStrings = all ? totalStrings : 48;
         String[] vstStrings = new Object[numStrings];
         int[] offsets = new int[numStrings];

         for (int i = 0; i < numStrings; i++) {
            int index = all ? i : VST_STR_INDICIES[i];
            offsets[i] = readInt(data, 48 + index * 4);
         }

         int position = 48 + totalStrings * 4;
         byte[] stringData = new byte[2048];

         for (int i = 0; i < numStrings; i++) {
            int offset = position + offsets[i];
            int length = 0;

            for (byte b = data[offset++]; b != 0 && b != 124; b = data[offset++]) {
               stringData[length++] = b;
            }

            if (length == 0) {
               System.out.println(((StringBuffer)(new Object("Bad string for "))).append(i).toString());
            }

            vstStrings[i] = (String)(new Object(stringData, 0, length, encoding));
         }

         return vstStrings;
      } finally {
         System.out.println("Unable to load strings");
         return null;
      }
   }

   private static final int readInt(byte[] b, int offset) {
      int b4 = b[offset++] & 255;
      int b3 = b[offset++] & 255;
      int b2 = b[offset++] & 255;
      int b1 = b[offset] & 255;
      return b1 << 24 | b2 << 16 | b3 << 8 | b4;
   }
